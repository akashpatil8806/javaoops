public class multidiArray {
    public static void main(String[] args) {
        int arr[][]={{1,2,4,6},{5,7,8,9}};
        System.out.println(" this is the multidimensionl array");
        for(int i=0;i<4;i++){
            for(int j=0;j<4;j++){
                System.out.println(arr[i][j]+" ");
            }
            System.out.println();  

        }
    }
}
